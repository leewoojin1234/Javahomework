package Number51_75;

public class Number51 {
    public static void main(String[] args) {
        fore(3, "Hello, world!");
        fore(5,"진주쌤 짱");
        fore(7,"2-1 최고!");
    }

    public static void fore(int a, String b) {
        for (int i = 1; i <= a; i++) {
            System.out.println(b);
        }
    }
}
